def build_prompt(news_list):
    articles = []
    for i, article in enumerate(news_list, start=1):
        articles.append(
            f"[기사 {i}]\n"
            f"제목: {article['title']}\n"
            f"설명: {article['summary']}\n"
        )

    joined = "\n".join(articles)

    prompt = f"""
너는 세계경제 뉴스 브리핑 전문가이자 투자 관점의 시장 분석가다.

아래 뉴스들을 기반으로 **투자 판단에 도움이 되도록 매우 직관적으로 정리해라.**

반드시 아래 형식을 정확히 지켜라.

📊 세계경제 핵심 브리핑

🔥 오늘 한줄 결론
- 전체 시장 상황을 한 문장으로 요약

⚠️ 리스크 요인 (3개 이하)
- 시장에 부담이 되는 요소

💡 기회 요인 (3개 이하)
- 긍정적인 흐름 또는 수혜 가능성

🌍 시장 영향
- 🇺🇸 미국 주식: 한 줄 요약
- 🇨🇳 중국 주식: 한 줄 요약
- 🇰🇷 한국 주식: 한 줄 요약
- 💵 달러: 한 줄 요약
- 🛢 유가/원자재: 한 줄 요약

📌 오늘 체크포인트 (3개)
- 투자자가 반드시 봐야 할 요소

📰 주요 뉴스 (최대 3개)
1. 자연스럽게 재작성된 제목
→ 왜 중요한지 한 줄

규칙:
1. 한국어로만 작성
2. 최대한 짧고 직관적으로 작성
3. 불필요한 설명 금지
4. 뉴스 나열 금지 (핵심만)
5. "가능성", "압력", "우려" 등 투자 관점 표현 사용
6. 국가별 주식 영향은 해당 국가 경제/수출/금리/환율 구조를 반영해 현실적으로 작성
7. 한국 시장은 수출/반도체/환율 영향을 고려해 작성
8. 중국 시장은 경기부양/내수/부동산/정책 흐름 고려
9. 미국 시장은 금리/유동성/빅테크/에너지 영향 고려
10. 전체 길이는 짧게 유지

기사 목록:
{joined}
""".strip()

    return prompt
